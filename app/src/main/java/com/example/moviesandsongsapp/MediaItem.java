package com.example.moviesandsongsapp;

public class MediaItem {

    private String image, title, description;

    public MediaItem(String image, String title, String description) {
        this.image = image;
        this.title = title;
        this.description = description;
    }

    public String getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }
}
